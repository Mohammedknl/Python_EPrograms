# Integers
x = 42
y = -123456789
z = x + y
print("x =", x)     # Output: x = 42
print("y =", y)     # Output: y = -123456789
print("z =", z)     # Output: z = -123456747
# Floating-point numbers
a = 3.14159
b = -1.5e3
c = a * b
print("a =", a)     # Output: a = 3.14159
print("b =", b)     # Output: b = -1500.0
print("c =", c)     # Output: c = -4711.385
# Basic math functions
abs_y = abs(y)
round_a = round(a, 2)
min_x_y = min(x, y)
max_a_b = max(a, b)

print("abs(y) =", abs_y)         # Output: abs(y) = 123456789
print("round(a, 2) =", round_a)  # Output: round(a, 2) = 3.14
print("min(x, y) =", min_x_y)    # Output: min(x, y) = -123456789
print("max(a, b) =", max_a_b)    # Output: max(a, b) = 3.14159
