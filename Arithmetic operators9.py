# Initialize variables
a = 10
b = 3
# Addition operator
c = a + b
print(a, "+", b, "=", c)   # Output: 10 + 3 = 13
# Subtraction operator
c = a - b
print(a, "-", b, "=", c)   # Output: 10 - 3 = 7
# Multiplication operator
c = a * b
print(a, "*", b, "=", c)   # Output: 10 * 3 = 30
# Division operator
c = a / b
print(a, "/", b, "=", c)   # Output: 10 / 3 = 3.3333333333333335
